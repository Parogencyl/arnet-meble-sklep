        względem Klienta, jeżeli Towar ma wadę fizyczną lub prawną (rękojmia).
        <br>
        2. Jeżeli Towar ma wadę, Klient może:
        <br>
        a) złożyć oświadczenie o obniżeniu ceny albo odstąpieniu od Umowy sprzedaży, chyba że Sprzedawca niezwłocznie i
        bez
        nadmiernych niedogodności dla Klienta wymieni Towar wadliwy na wolny od wad albo wadę usunie.
        Ograniczenie to nie ma zastosowania, jeżeli Towar był już wymieniony lub naprawiany przez Sprzedawcę albo
        Sprzedawca
        nie uczynił zadość obowiązkowi wymiany Towaru na wolny od wad lub usunięcia wad. Klient może zamiast
        zaproponowanego
        przez Sprzedawcę usunięcia wady żądać wymiany Towaru na wolny od wad albo zamiast wymiany Towaru żądać usunięcia
        wady, chyba że doprowadzenie rzeczy do zgodności z umową w sposób wybrany przez Klienta jest niemożliwe albo
        wymagałoby nadmiernych kosztów w porównaniu ze sposobem proponowanym przez Sprzedawcę. Przy ocenie nadmierności
        kosztów uwzględnia się wartość Towaru wolnego od wad, rodzaj i znaczenie stwierdzonej wady, a także bierze się
        pod
        uwagę niedogodności, na jakie narażałby Klienta inny sposób zaspokojenia.
        <br>
        b) żądać wymiany Towaru wadliwego na wolny od wad albo usunięcia wady. Sprzedawca jest obowiązany wymienić Towar
        wadliwy na wolny od wad lub usunąć wadę w rozsądnym czasie bez nadmiernych niedogodności dla Klienta.
        Sprzedawca może odmówić zadośćuczynienia żądaniu Klienta, jeżeli doprowadzenie do zgodności z Umową sprzedaży
        Towaru
        wadliwego w sposób wybrany przez Klienta jest niemożliwe lub w porównaniu z drugim możliwym sposobem
        doprowadzenia
        do zgodności z Umową sprzedaży wymagałoby nadmiernych kosztów. Koszty naprawy lub wymiany ponosi Sprzedawca.
