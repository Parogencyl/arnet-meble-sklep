        zdarzenie nastąpi wcześniej.
        <br>
        6. Jeżeli Konsument korzystający z prawa do odstąpienia wybrał sposób dostarczenia Towaru inny niż najtańszy
        zwykły
        sposób Dostawy oferowany przez Sprzedawcę, Sprzedawca nie jest zobowiązany do zwrotu Konsumentowi poniesionych
        przez
        niego dodatkowych kosztów.
        <br>
        7. Konsument ma obowiązek zwrócić Towar Sprzedawcy niezwłocznie, jednak nie później niż w terminie 14 dni od
        dnia, w
        którym odstąpił od Umowy sprzedaży. Do zachowania terminu wystarczy odesłanie Towaru na adres Sprzedawcy przed
        upływem tego terminu.
        <br>
        8. W wypadku odstąpienia Klient będący Konsumentem ponosi tylko bezpośrednie koszty zwrotu Towaru.
        <br>
        9. Jeśli ze względu na swój charakter Towar nie może zostać w zwykłym trybie odesłany pocztą, Sprzedawca
        informuje
        Konsumenta o kosztach zwrotu rzeczy na Stronie Internetowej Sklepu.
        <br>
        10. Konsument ponosi odpowiedzialność za zmniejszenie wartości Towaru będące wynikiem korzystania z niego w
        sposób
        wykraczający poza sposób konieczny do stwierdzenia charakteru, cech i funkcjonowania Towaru.
        <br>
        11. Sprzedawca dokonuje zwrotu płatności przy użyciu takiego samego sposobu zapłaty, jakiego użył Konsument
        chyba,
        że Konsument wyraźnie zgodził się na inny sposób zwrotu, który nie wiąże się dla niego z żadnymi kosztami.
        <br>
        12. Prawo odstąpienia od Umowy sprzedaży nie przysługuje Klientowi będącemu Konsumentem w odniesieniu do umów, w
        których Towarem jest rzecz nieprefabrykowana, wyprodukowana według specyfikacji Konsumenta lub służąca
        zaspokojeniu
        jego zindywidualizowanych potrzeb.
    </p>
    <p>
        <b> § 10 Usługi nieodpłatne </b>
        <br>
        1. Sprzedawca świadczy na rzecz Klientów, drogą elektroniczną usługi nieodpłatne:
        <br>
        a) Formularz kontaktowy;
        <br>
        b) Prowadzenie Konta Klienta.
        <br>
        2. Usługi wskazane w §11 ust. 1 powyżej świadczone są 7 dni w tygodniu, 24 godziny na dobę.
        <br>
        3. Sprzedawca zastrzega sobie możliwość wyboru i zmiany rodzaju, form, czasu oraz sposobu udzielania dostępu do
        wybranych wymienionych usług, o czym poinformuje Klientów w sposób właściwy dla zmiany Regulaminu.
        <br>
        4. Usługa Formularz kontaktowy polega na wysłaniu za pomocą formularza umieszczonego na Stronie Internetowej
        Sklepu
        wiadomości do Sprzedawcy.
        <br>
        5. Rezygnacja z usługi nieodpłatnej Formularz kontaktowy, możliwa jest w każdej chwili i polega na zaprzestaniu
        wysyłania zapytań do Sprzedawcy.
        <br>
        10. Usługa Prowadzenie Konta Klienta dostępna jest po dokonaniu Rejestracji na zasadach opisanych w Regulaminie
        i
        polega na udostępnieniu Klientowi dedykowanego panelu w ramach Strony Internetowej Sklepu, umożliwiającego
        Klientowi
        modyfikacje danych, które podał podczas Rejestracji, jak też śledzenia historii
        zamówień już zrealizowanych.
        <br>
        11. Klient, który dokonał Rejestracji może zgłosić Sprzedawcy żądanie usunięcia Konta Klienta, przy czym w
        przypadku
        zgłoszenia żądania usunięcia Konta Klienta przez Sprzedawcę, może ono zostać usunięte do 14 dni od zgłoszenia
        żądania.
        <br>
        12. Sprzedawca jest uprawniony do zablokowania dostępu do Konta Klienta i usług nieodpłatnych, w przypadku
        działania
        przez Klienta na szkodę Sprzedawcy lub innych Klientów, naruszenia przez Klienta przepisów prawa lub postanowień
        Regulaminu, a także, gdy zablokowanie dostępu do Konta Klienta i usług nieodpłatnych jest uzasadnione względami
        bezpieczeństwa - w szczególności: przełamywaniem przez Klienta zabezpieczeń Strony Internetowej Sklepu lub
        innymi
        działaniami hakerskimi. Zablokowanie dostępu do Konta Klienta i usług nieodpłatnych z wymienionych przyczyn trwa
        przez okres niezbędny do rozwiązania kwestii stanowiącej podstawę zablokowania dostępu do Konta Klienta i usług
        nieodpłatnych. Sprzedawca zawiadamia Klienta o zablokowaniu dostępu do Konta Klienta i usług nieodpłatnych drogą
        elektroniczną na adres podany przez Klienta w formularzu rejestracyjnym.
    </p>
    <p>
        <b> § 11 Rozwiązanie umowy (nie dotyczy Umów sprzedaży)</b>
        <br>
        1. Zarówno Klient, jak i Sprzedawca mogą rozwiązać umowę o świadczenie usług drogą elektroniczną w każdym czasie
        i
        bez podania przyczyn, z zastrzeżeniem zachowania praw nabytych przez drugą stronę przed rozwiązaniem ww. umowy
        oraz
        postanowień poniżej.
        <br>
        2. Klient, który dokonał Rejestracji rozwiązuje umowę o świadczenie usług drogą elektroniczną, poprzez wysłanie
        do
        Sprzedawcy stosownego oświadczenia woli, przy użyciu dowolnego środka komunikacji na odległość, umożliwiającego
        zapoznanie się Sprzedawcy z oświadczeniem woli Klienta.
        <br>
        3. Sprzedawca wypowiada umowę o świadczenie usług drogą elektroniczną poprzez wysłanie do Klienta stosownego
        oświadczenia woli na adres poczty elektronicznej podany przez Klienta podczas Rejestracji.
    </p>
    <p>
        <b>§ 12 Postanowienia końcowe </b>
        <br>
        1. Sprzedawca ponosi odpowiedzialność z tytułu niewykonania lub nienależytego wykonania umowy, lecz w przypadku
        umów
        zawieranych z Klientami będącymi Przedsiębiorcami Sprzedawca ponosi odpowiedzialność tylko w przypadku umyślnego
        wyrządzenia szkody i w granicach rzeczywiście poniesionych strat przez Klienta będącego Przedsiębiorcą.
        <br>
        2. Treść niniejszego Regulaminu może zostać utrwalona poprzez wydrukowanie, zapisanie na nośniku lub pobranie w
        każdej chwili ze Strony Internetowej Sklepu.
        <br>
        3. W przypadku powstania sporu na gruncie zawartej Umowy sprzedaży, strony będą dążyły do rozwiązania sprawy
        polubownie. Prawem właściwym dla rozstrzygania wszelkich sporów powstałych na gruncie niniejszego Regulaminu
        jest
        prawo polskie.
        <br>
        4. Sprzedawca informuje Klienta będącego Konsumentem o możliwości skorzystania z pozasądowych sposobów
        rozpatrywania
        reklamacji i dochodzenia roszczeń. Zasady dostępu do tych procedur dostępne są w siedzibach lub na stronach
        internetowych podmiotów uprawnionych do pozasądowego rozpatrywania sporów. Mogą nimi być w szczególności
        rzecznicy
        praw konsumenta lub Wojewódzkie Inspektoraty Inspekcji Handlowej, których lista jest dostępna na stronie
        internetowej Urzędu Ochrony Konkurencji i Konsumentów.
        Sprzedawca informuje, że pod adresem http://ec.europa.eu/consumers/odr/ dostępna jest platforma internetowego
        systemu rozstrzygania sporów pomiędzy konsumentami i przedsiębiorcami na szczeblu unijnym (platforma ODR).
        <br>
        5. Sprzedawca zastrzega sobie prawo zmiany niniejszego Regulaminu. Wszystkie zamówienia przyjęte przez
        Sprzedawcę do
        realizacji przed dniem wejścia w życie nowego Regulaminu są realizowane na podstawie Regulaminu, który
        obowiązywał w
        dniu składania zamówienia przez Klienta. Zmiana Regulaminu wchodzi w życie w terminie 7 dni od dnia
        opublikowania na
        Stronie Internetowej Sklepu. Sprzedawca poinformuje Klienta na 7 dni przed wejściem w życie nowego Regulaminu o
        zmianie Regulaminu za pomocą wiadomości przesłanej drogą elektroniczną zawierającej odnośnik do tekstu
        zmienionego
        Regulaminu. W razie, gdy Klient nie akceptuje nowej treści Regulaminu obowiązany jest zawiadomić o tym fakcie
        Sprzedawcę, co skutkuje rozwiązaniem umowy zgodnie z postanowieniami §13 Regulaminu.
        <br>
        6. Regulamin wchodzi w życie z dniem 01.01.2021 r.
    </p>

