        upływem
        roku, lecz termin ten nie może zakończyć się przed upływem terminu określonego w zdaniu pierwszym. W tym
        terminie
        Klient może odstąpić od Umowy sprzedaży lub złożyć oświadczenie o obniżeniu ceny z powodu wady Towaru. Jeżeli
        Klient
